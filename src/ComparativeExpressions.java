public class ComparativeExpressions {
    public static void main (String[] args) {
        int bread = 10;
        int sugar = 20;

        System.out.println(bread == sugar);
        System.out.println(bread != sugar);
        System.out.println(bread > sugar);
        System.out.println(bread < sugar);
        System.out.println(bread >= sugar);
        System.out.println(bread <= sugar);

    }
}
